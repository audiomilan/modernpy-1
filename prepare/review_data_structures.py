# Lists.

todo = [ "pick up here", "buy groceries", "pay bills" ]

for i in todo:
    print(i)

# Dictionaries.

User = { 
    "name" : "Jeriah", 
    "age": 27, 
    "email" : "someemail@email.com"
}

# Loop over Dictionary.
for i in User.items():
    print(i)

tuples = ("i", "am", "a", "tuple", "of", "strings")

for i in tuples:
    print(i)
